from typing import List


class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        walked = set()
        m = len(grid)
        n = len(grid[0])
        cnt = 0
        for i in range(m):
            for j in range(n):
                if grid[i][j] == '0' or (i, j) in walked:
                    continue
                # 这个位置位1
                walked.add((i, j))

                stack = [(i, j)]
                while stack:
                    ii, jj = stack.pop()
                    print(f'({ii},{jj})', end=' ')
                    for di, dj in [(-1, 0), (1, 0), (0, 1), (0, -1)]:
                        x = ii + di
                        y = jj + dj
                        if not (0 <= x < m and 0 <= y < n):
                            continue
                        if (x, y) not in walked and grid[x][y] == '1':
                            stack.append((x, y))
                            walked.add((x, y))

                cnt += 1
                print()

        return cnt


